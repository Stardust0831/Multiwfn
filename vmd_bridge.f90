subroutine open_vmd_scene_file(scenefile,sceneunit,lopen,exportfile)
use defvar
implicit real*8 (a-h,o-z)
character(len=200),intent(out) :: scenefile
integer,intent(out) :: sceneunit
logical,intent(out) :: lopen
character(len=*),intent(in),optional :: exportfile
integer iopen

scenefile=vmdscenefile
if (scenefile==" ") scenefile="multiwfn_scene.tcl"
if (trim(scenefile)=="auto".or.trim(scenefile)=="Auto".or.trim(scenefile)=="AUTO") then
    scenefile="multiwfn_scene.tcl"
    if (present(exportfile)) then
        if (trim(exportfile)/=" ") scenefile=trim(exportfile)//".vmd.tcl"
    end if
end if

sceneunit=-1
open(newunit=sceneunit,file=scenefile,status="replace",iostat=iopen)
if (iopen/=0) then
    write(*,"(' VMD scene script was not written because the file could not be opened: ',a)") trim(scenefile)
    write(*,"(' VMD scene open IOSTAT = ',i0)") iopen
    lopen=.false.
    return
end if

lopen=.true.

end subroutine


subroutine write_vmd_scene_header(ifileid,scenefile)
implicit real*8 (a-h,o-z)
integer,intent(in) :: ifileid
character(len=*),intent(in) :: scenefile
character(len=600) c600scene

write(ifileid,"(a)") "# VMD scene generated by Multiwfn"
c600scene=vmd_tcl_dquote(scenefile)
write(ifileid,"(a)") "# Load this script in VMD by: source "//trim(c600scene)
write(ifileid,"(a)") "# Relative file paths are resolved by VMD from its current working directory."
write(ifileid,"(a)") "# If loading manually, source this scene from the directory used for the Multiwfn export or use absolute file paths."

end subroutine


subroutine write_vmd_scene_footer(ifileid)
implicit real*8 (a-h,o-z)
integer,intent(in) :: ifileid

write(ifileid,"(a)") "display projection Orthographic"
write(ifileid,"(a)") "axes location Off"
write(ifileid,"(a)") "display resetview"

end subroutine


subroutine write_vmd_structure_molecule(ifileid,structfile,filetype)
implicit real*8 (a-h,o-z)
integer,intent(in) :: ifileid
character(len=*),intent(in) :: structfile,filetype
character(len=600) c600struct,c600type

c600struct=vmd_tcl_dquote(structfile)
c600type=vmd_tcl_dquote(filetype)

write(ifileid,"(a)") ""
write(ifileid,"(a)") "# Structure file: "//trim(structfile)
write(ifileid,"(a)") "mol new "//trim(c600struct)//" type "//trim(c600type)//" waitfor all"
write(ifileid,"(a)") "mol delrep 0 top"
write(ifileid,"(a)") "mol representation CPK 1.000000 0.300000 16 16"
if (trim(filetype)=="pqr".or.trim(filetype)=="PQR") then
    write(ifileid,"(a)") "mol color Charge"
else
    write(ifileid,"(a)") "mol color Element"
end if
write(ifileid,"(a)") "mol material Opaque"
write(ifileid,"(a)") "mol addrep top"

end subroutine


